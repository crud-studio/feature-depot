/**
 * Generate a friendly looking, non unique ID
 * @param adjectiveCount the amount of adjectives to add
 * @param addNumber whether or not to append a double digit number to the end of the ID
 * @return a randomly generated friendly ID
 */
fun generateFriendlyId(adjectiveCount: Int = 2, addNumber: Boolean = false): String {
    val adjectives = mutableListOf<String>()
    for (i in 1..adjectiveCount) {
        adjectives += ADJECTIVES.random().toLowerCase()
    }

    var id = "${adjectives.joinToString("-")}-${NOUNS.random().toLowerCase()}"
    if(addNumber) {
        id += "-${Random.nextInt(100)}"
    }

    return id
}
